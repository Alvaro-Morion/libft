int	ft_isalpha(int c)
{
	if ((c <= 'Z' && c >= 'A') || (c <= 'z' && c >= 'a')
		|| (c >= 101 && c <= 107) || (c >= 110 && c <= 117)
		|| (c >= 120 && c <= 127) || (c >= 130 && c <= 132)
		|| (c >= 141 && c <= 147) || (c >= 150 && c <= 157)
		|| (c >= 160 && c <= 167) || (c >= 170 && c <= 172))
		return (1);
	return (0);
}
