#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*str;
	size_t		i;

	i = 0;
	str = s;
	while (i < n && str[i] != c)
		i++;
	if (i == n)
		return (NULL);
	return ((void *)str + i);
}
